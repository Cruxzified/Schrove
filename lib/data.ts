// Generate 30-day attendance heatmap
export function generateHeatmapData() {
  const days = [];
  const now = new Date(2026, 4, 9); // May 9, 2026
  for (let i = 29; i >= 0; i--) {
    const d = new Date(now);
    d.setDate(d.getDate() - i);
    const isWeekend = d.getDay() === 0 || d.getDay() === 6;
    const rate = isWeekend ? 0 : Math.floor(Math.random() * 25 + 75);
    days.push({
      date: d.toISOString().split('T')[0],
      rate: isWeekend ? 0 : rate,
      present: isWeekend ? 0 : Math.floor(rate * 4.2),
      total: isWeekend ? 0 : 420,
    });
  }
  return days;
}

